    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        TapChiService us = new TapChiService();
        int row = tapChiTableList.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(TapChiFrame.this, "Vui lòng chọn Tạp Chí muốn xóa!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            int comfirm = JOptionPane.showConfirmDialog(TapChiFrame.this, "Bạn có chắc chắn muốn xóa!");

            if (comfirm == JOptionPane.YES_OPTION) {
                int ID = Integer.valueOf(String.valueOf(tapChiTableList.getValueAt(row, 0)));

                try {
                    us.deleteTapChi(ID);
                } catch (SQLException ex) {
                    java.util.logging.Logger.getLogger(TapChiFrame.class.getName()).log(Level.SEVERE, null, ex);
                }

                try {
                    setData(us.getAllTapChi());
                } catch (SQLException ex) {
                    java.util.logging.Logger.getLogger(TapChiFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

