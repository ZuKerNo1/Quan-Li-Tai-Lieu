    private void setData(List<TapChi> tapChis) throws SQLException {
        DefaultTableModel defaultTableModel;
        TapChiService us = new TapChiService();
        defaultTableModel = new DefaultTableModel();
        tapChiTableList.setModel(defaultTableModel);
        defaultTableModel.addColumn("id Tài Liệu");
        defaultTableModel.addColumn("Tên NXB");
        defaultTableModel.addColumn("Số Bản Phát Hành");
        defaultTableModel.addColumn("Số Phát Hành");
        defaultTableModel.addColumn("Tháng Phát Hành");
        for (TapChi tapchi : tapChis) {
            defaultTableModel.addRow(new Object[]{tapchi.getIdTaiLieu(), tapchi.getTenNXB(), tapchi.getSoBanPhatHanh(),
                tapchi.getSoPhatHanh(), tapchi.getThangPhatHanh()});
        }
    }
