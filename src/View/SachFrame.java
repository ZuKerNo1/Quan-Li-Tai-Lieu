    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        SachService us = new SachService();
        int row = sachTableList.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(SachFrame.this, "Vui lòng chọn Sách muốn xóa!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            int comfirm = JOptionPane.showConfirmDialog(SachFrame.this, "Bạn có chắc chắn muốn xóa!");

            if (comfirm == JOptionPane.YES_OPTION) {
                int ID = Integer.valueOf(String.valueOf(sachTableList.getValueAt(row, 0)));

                try {
                    us.deleteSach(ID);
                } catch (SQLException ex) {
                    java.util.logging.Logger.getLogger(SachFrame.class.getName()).log(Level.SEVERE, null, ex);
                }

                try {
                    setData(us.getAllSach());
                } catch (SQLException ex) {
                    java.util.logging.Logger.getLogger(SachFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

