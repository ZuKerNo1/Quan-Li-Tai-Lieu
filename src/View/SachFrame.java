    private void setData(List<Sach> sachs) throws SQLException {
        DefaultTableModel defaultTableModel;
        SachService us = new SachService();
        defaultTableModel = new DefaultTableModel();
        sachTableList.setModel(defaultTableModel);
        defaultTableModel.addColumn("id Tài Liệu");
        defaultTableModel.addColumn("Tên NXB");
        defaultTableModel.addColumn("Số Bản Phát Hành");
        defaultTableModel.addColumn("Tên tác giả");
        defaultTableModel.addColumn("Số Trang");
        for (Sach sach : sachs) {
            defaultTableModel.addRow(new Object[]{sach.getIdTaiLieu(), sach.getTenNXB(), sach.getSoBanPhatHanh(),
                sach.getTenTacGia(), sach.getSoTrang()});
        }
    }
